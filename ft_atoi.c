#include "libft.h"

int	ft_atoi(const char *str)
{
	int	c;
	int	s;
	int	r;

	c = 0;
	s = 1;
	r = 0;
	while (str[c] == ' ' || str[c] == '\n' || str[c] == '\t'
		|| str[c] == '\v' || str[c] == '\f' || str[c] == '\r')
		c++;
	if (str[c] == '-' || str[c] == '+')
	{
		if (str[c] == '-')
			s = -1;
		c++;
	}
	while (str[c] >= '0' && str[c] <= '9')
	{
		r = (r * 10) + (str[c] - '0');
		c++;
	}
	return (r * s);
}
