#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*i;

	i = 0;
	while (*s)
	{
		if (*s == (char)c)
			i = (char *)s;
		s++;
	}
	if (*s == (char)c)
		return ((char *)s);
	return (i);
}
